function renderInsights(insights) {
  if (!insights) return null;

  const lines = insights.split('\n');
  const sections = [];
  let currentSection = null;
  let currentContent = [];

  lines.forEach((line) => {
    const trimmed = line.trim();
    
    if (/###\s+\d+\.?\s+/.test(trimmed)) {
      if (currentContent.length > 0 && currentSection) {
        sections.push({
          title: currentSection,
          content: currentContent.join('\n')
        });
      }
      const match = trimmed.match(/###\s+\d+\.?\s+([^\n]+)/);
      currentSection = match ? match[1] : '';
      currentContent = [];
    } else if (trimmed && !trimmed.startsWith('###')) {
      currentContent.push(trimmed);
    }
  });

  if (currentContent.length > 0 && currentSection) {
    sections.push({
      title: currentSection,
      content: currentContent.join('\n')
    });
  }

  return (
    <div>
      {sections.map((section, idx) => (
        <div key={idx} style={{ marginBottom: '12px', pageBreakInside: 'avoid' }}>
          <h3 style={{
            fontSize: '11pt',
            fontWeight: '600',
            color: '#333',
            margin: '10px 0 5px 0',
            paddingBottom: '3px',
            borderBottom: '1px solid #ddd'
          }}>
            {section.title}
          </h3>
          <p style={{
            fontSize: '10pt',
            lineHeight: '1.6',
            color: '#444',
            margin: '5px 0 0 0',
            whiteSpace: 'pre-wrap',
            wordWrap: 'break-word'
          }}>
            {section.content}
          </p>
        </div>
      ))}
    </div>
  );
}

